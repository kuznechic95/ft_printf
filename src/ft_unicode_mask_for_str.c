#include "../inc/ft_printflib.h"

void		ft_unicode_mask_for_str(wchar_t c, char **str)
{
	if (c <= 0b1111111)
		*(*str)++ = c;
	else if (c <= 0b11111111111)
	{
		*(*str)++ = (c >> 6) + 0b11000000;
		*(*str)++ = (c & 0b111111) + 0b10000000;
	}
	else if (c <= 0b1111111111111111)
	{
		*(*str)++ = (c >> 12) + 0b11100000;
		*(*str)++ = ((c >> 6) & 0b111111) + 0b10000000;
		*(*str)++ = (c & 0b111111) + 0b10000000;
	}
	else if (c <= 0b100001111111111111111)
	{
		*(*str)++ = (c >> 18) + 0b11110000;
		*(*str)++ = ((c >> 12) & 0b111111) + 0b10000000;
		*(*str)++ = ((c >> 6) & 0b111111) + 0b10000000;
		*(*str)++ = (c & 0b111111) + 10000000;
	}
}

/*
** UNICODE masks
** 0xxxxxxx
** 110xxxxx 10xxxxxx
** 1110xxxx 10xxxxxx 10xxxxxx
** 11110xxx 10xxxxxx 10xxxxxx 10xxxxxx
*/
